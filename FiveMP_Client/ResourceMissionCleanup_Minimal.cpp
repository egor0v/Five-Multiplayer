SCRIPT_PROPS GetScriptProp(const char *name) {
	for (int i = 0; i<sizeof(scriptnames) / sizeof(ScriptInfo); i++) {
		if (_strcmpi(name, scriptnames[i].name) == 0) {
			return scriptnames[i].proc;
		}
	}
	return SCRIPT_DISABLED;
}

void detachMissions() {
	for (int i = 0; i<sizeof(scriptnames) / sizeof(ScriptInfo); i++) {
		if (scriptnames[i].proc == SCRIPT_DISABLED) {
			PLAYER::FORCE_CLEANUP_FOR_ALL_THREADS_WITH_THIS_NAME(scriptnames[i].name, 8); //Cleanup first
			GAMEPLAY::TERMINATE_ALL_SCRIPTS_WITH_THIS_NAME(scriptnames[i].name); //Terminate
		}
	}
}

void missionSetThread() {
	rage::pgPtrCollection<GtaThread>* threadCollection = NULL;
	if (!pThreadCopy) return;

	while (!threadCollection) {
		threadCollection = GetGtaThreadCollection(pBlipList);
		Sleep(10);
		continue;
	}

	for (UINT16 i = 0; i < threadCollection->count(); i++) {
		GtaThread* pThread = threadCollection->at(i);
		if (pThread->GetScriptHandler() != pThreadCopy->GetScriptHandler())
			pThread->Kill();
	}
}
